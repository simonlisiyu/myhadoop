package com.lsy.myhadoop.sparkjava.sql.commons;

import java.io.Serializable;

/*
    * JavaBean
    * */
    public class Student implements Serializable {

    private static final long serialVersionUID = -2620220282757151059L;

    private int id;
        private String name;
        private int age;

        public int getId() {
            return id;
        }

        public void setId(int id) {
            this.id = id;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}